package friedman.earthquakes;

public class Features {
	
	private String type;
	private Properties[] properties;
	private Geometry[] geometry;
	private String id;
	
	public String getType() {
		return type;
	}
	public Properties[] getProperties() {
		return properties;
	}
	public Geometry[] getGeometry() {
		return geometry;
	}
	public String getId() {
		return id;
	}
	
	
}
