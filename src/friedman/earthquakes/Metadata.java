package friedman.earthquakes;

import java.net.URL;

public class Metadata {
	
	private int generated;
	private URL url;
	private String title;
	private int status;
	private String api;
	private int count;
	
	public int getGenerated() {
		return generated;
	}
	public URL getUrl() {
		return url;
	}
	public String getTitle() {
		return title;
	}
	public int getStatus() {
		return status;
	}
	public String getApi() {
		return api;
	}
	public int getCount() {
		return count;
	}
}
